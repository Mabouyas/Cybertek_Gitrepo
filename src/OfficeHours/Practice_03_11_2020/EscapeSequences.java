package OfficeHours.Practice_03_11_2020;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("   Java is a programming language");
        System.out.println("\tJava is a programming language");
        System.out.println("\n\tJava is a programming language");
        System.out.println("Java is a programming language \" My favorite\" ");
        System.out.println(" I love you \" You're the best \" ");
        System.out.println(" C:\\Program Files");
    }
}

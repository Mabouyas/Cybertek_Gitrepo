package day02_Variables;

public class MyTriangle {
    public static void main(String[] args) {
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");
        System.out.println();
        System.out.println("      *");
        System.out.println("    *  *");
        System.out.println("  *  *  *");
        System.out.println(" * * * * *");
        System.out.println("* * * * * *");
    }
}
